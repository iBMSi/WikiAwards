function pesquisar() {
    console.log(anos);

    let campoPesquisa = document.getElementById("campo-pesquisa").value;

    let section = document.getElementById("resultados-pesquisa");
    let resultados = "";
    let titulo = "";
    let descrição = "";

    campoPesquisa = campoPesquisa.toLowerCase();

    for (let ano of anos) {
        titulo = ano.JogodoAno.toLowerCase();  // Assumindo que 'titulo' é o 'JogodoAno'
        descrição = ano.tag.toLowerCase();  // Assumindo que 'descrição' é a 'MelhorNarrativa'

        if (campoPesquisa === "") {
            section.innerHTML = "Nada para procurar";
            return;
        }

        if (titulo.includes(campoPesquisa) || descrição.includes(campoPesquisa)) {
            resultados += `
                <div class="item-resultado">
                    <h2>
                        <p><strong>Ano:</strong> ${ano.Ano}</p>
                        <p><strong>Jogo do Ano:</strong> ${ano.JogodoAno}</p>
                        <p><strong>Melhor Direção:</strong> ${ano.MelhorDireção}</p>
                        <p><strong>Melhor Narrativa:</strong> ${ano.MelhorNarrativa}</p>
                        <p><strong>Melhor Atuação:</strong> ${ano.MelhorAtuação}</p>
                        <p><strong>Melhor Trilha Sonora:</strong> ${ano.MelhorTrilhaSonora}</p>
                        <p><strong>Melhor Design de Áudio:</strong> ${ano.MelhorDesigndeaudio}</p>
                        <p><strong>Melhor Design de Jogo:</strong> ${ano.MelhorDesigndeJogo}</p>
                        <p><strong>Melhor Jogo Independente:</strong> ${ano.MelhorJogoIndependente}</p>
                        <p><strong>Melhor Jogo de Realidade Virtual:</strong> ${ano.MelhorJogodeRealidadeVirtual}</p>
                        <p><strong>Tag:</strong> ${ano.tag}</p>
                    </h2>
                </div>`;
        }
    }

    if (resultados === "") {
        section.innerHTML = "Nenhum resultado encontrado";
    } else {
        section.innerHTML = resultados;
    }
}
